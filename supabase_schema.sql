alter table categories add column if not exists seed_batch text default '';
alter table ads add column if not exists seed_batch text default '';
alter table order_items add column if not exists size text default '';
alter table order_items alter column product_id drop not null;
alter table order_items drop constraint if exists order_items_product_id_fkey;
alter table order_items add constraint order_items_product_id_fkey foreign key (product_id) references products(id) on delete set null;
alter table orders alter column customer_id drop not null;
alter table orders drop constraint if exists orders_customer_id_fkey;
alter table orders add constraint orders_customer_id_fkey foreign key (customer_id) references customers(id) on delete set null;
alter table orders add column if not exists staff_id bigint references staff_users(id) on delete set null;
