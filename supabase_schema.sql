create index if not exists idx_orders_customer on orders(customer_id);
create index if not exists idx_orders_source_created on orders(source, created_at);
create index if not exists idx_orders_staff on orders(staff_id);
create index if not exists idx_order_items_order on order_items(order_id);
create index if not exists idx_staff_users_active on staff_users(active);
create index if not exists idx_inventory_movements_product on inventory_movements(product_id, created_at);
create index if not exists idx_inventory_movements_reference on inventory_movements(reference_type, reference_id);
create unique index if not exists idx_orders_receipt_number on orders(receipt_number) where receipt_number is not null;

