begin;

create table if not exists admin_users (
  id bigint generated by default as identity primary key,
  username text unique not null,
  password_hash text not null,
  created_at text not null
);

create table if not exists staff_users (
  id bigint generated by default as identity primary key,
  name text not null,
  username text unique not null,
  password_hash text not null,
  role text default 'Receptionist',
  permissions jsonb default '[]'::jsonb,
  active boolean default true,
  created_by bigint references admin_users(id) on delete set null,
  created_at text not null,
  updated_at text not null,
  last_login_at text
);

create table if not exists customers (
  id bigint generated by default as identity primary key,
  name text not null,
  email text unique not null,
  password_hash text not null,
  created_at text not null
);

create table if not exists sessions (
  token text primary key,
  user_type text not null,
  user_id bigint not null,
  created_at text not null
);

create table if not exists categories (
  id bigint generated by default as identity primary key,
  name text unique not null,
  description text default '',
  price_mode text default 'range',
  sort_order integer default 0,
  seed_batch text default '',
  created_at text not null
);

create table if not exists products (
  id bigint generated by default as identity primary key,
  category_id bigint references categories(id) on delete set null,
  name text not null,
  price numeric(10,2) not null,
  old_price numeric(10,2),
  badge text default '',
  rating text default '4.8',
  stock integer default 0,
  product_sizes jsonb default '[]'::jsonb,
  image text not null,
  images jsonb default '[]'::jsonb,
  crop text default 'center',
  description text default '',
  ai_type text default 'top',
  ai_images jsonb default '[]'::jsonb,
  ai_prompts jsonb default '[]'::jsonb,
  active boolean default true,
  seed_batch text default '',
  created_at text not null
);

create table if not exists ads (
  id bigint generated by default as identity primary key,
  title text not null,
  subtitle text default '',
  button_text text default 'Shop Now',
  link text default '#/shop',
  image text not null,
  active boolean default true,
  sort_order integer default 0,
  seed_batch text default '',
  created_at text not null
);

create table if not exists orders (
  id bigint generated by default as identity primary key,
  customer_id bigint references customers(id) on delete set null,
  staff_id bigint references staff_users(id) on delete set null,
  customer_name text not null,
  phone text not null,
  address text default '',
  status text default 'Processing',
  source text default 'online',
  receipt_number text unique,
  payment_method text default '',
  payment_status text default 'Pending',
  subtotal numeric(10,2) default 0,
  discount numeric(10,2) default 0,
  amount_paid numeric(10,2) default 0,
  change_due numeric(10,2) default 0,
  notes text default '',
  voided_at text,
  voided_by bigint references staff_users(id) on delete set null,
  total numeric(10,2) not null,
  created_at text not null
);

create table if not exists order_items (
  id bigint generated by default as identity primary key,
  order_id bigint not null references orders(id) on delete cascade,
  product_id bigint references products(id) on delete set null,
  product_name text not null,
  product_image text default '',
  price numeric(10,2) not null,
  requested_qty integer default 1,
  qty integer not null,
  size text default '',
  status text default 'Processing'
);

create table if not exists settings (
  key text primary key,
  value text not null
);

create table if not exists newsletter_subscribers (
  id bigint generated by default as identity primary key,
  phone text unique not null,
  created_at text not null
);

create table if not exists inventory_movements (
  id bigint generated by default as identity primary key,
  product_id bigint references products(id) on delete set null,
  product_name text default '',
  size text default '',
  quantity_delta integer not null,
  movement_type text not null,
  reference_type text default '',
  reference_id bigint,
  performed_by_type text default '',
  performed_by_id bigint,
  performed_by_name text default '',
  note text default '',
  created_at text not null
);

